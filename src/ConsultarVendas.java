   public void carregarTabelaVendidos() {
    ProdutosDAO dao = new ProdutosDAO();
    List<ProdutosDTO> listaProdutos = dao.listarVendas();

    DefaultTableModel modelo = (DefaultTableModel)listaVendidos.getModel();
    modelo.setRowCount(0); // Limpa tabela

    for (ProdutosDTO p : listaProdutos) {
        modelo.addRow(new Object[]{
            p.getId(),
            p.getNome(),
            p.getValor(),
            p.getStatus()
        });
    }
   }

